module.exports = {
    somar(a,b) {
        console.log(`A soma de ${a} + ${b} = ${(a+b).toFixed(2)}`)
    },
    subtrair(a,b) {
        console.log(`A soma de ${a} - ${b} = ${(a-b).toFixed(2)}`)
    },
    dividir(a,b) {
        console.log(`A soma de ${a} / ${b} = ${(a/b).toFixed(2)}`)
    },
    multiplicar(a,b) {
        console.log(`A soma de ${a} * ${b} = ${(a*b).toFixed(2)}`)
    }
}